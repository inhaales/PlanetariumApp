USE PlanetariumDB;
GO

-- Таблиця 1: Словник астрономічних термінів
CREATE TABLE Glossary (
    Id INT IDENTITY(1,1) PRIMARY KEY,
    Term NVARCHAR(100) NOT NULL,
    Definition NVARCHAR(MAX) NOT NULL,
    Category NVARCHAR(100) NOT NULL
);

-- Таблиця 2: Сузір'я зоряного неба
CREATE TABLE Constellations (
    Id INT IDENTITY(1,1) PRIMARY KEY,
    Name NVARCHAR(100) NOT NULL,
    LatinName NVARCHAR(100) NOT NULL,
    Symbolism NVARCHAR(MAX) NOT NULL,
    BestWatchingTime NVARCHAR(100) NOT NULL,
    ImagePath NVARCHAR(100) NULL
);

-- Таблиця 3: Космічні об'єкти
CREATE TABLE CelestialBodies (
    Id INT IDENTITY(1,1) PRIMARY KEY,
    Name NVARCHAR(100) NOT NULL,
    Type NVARCHAR(100) NOT NULL,
    Description NVARCHAR(MAX) NOT NULL,
    Mass NVARCHAR(50) NOT NULL,
    DistanceFromEarth NVARCHAR(50) NOT NULL,
    ConstellationId INT NULL,
    ImagePath NVARCHAR(100) NULL,
    CONSTRAINT FK_CelestialBodies_Constellations FOREIGN KEY (ConstellationId) 
        REFERENCES Constellations(Id) ON DELETE SET NULL
);

-- Таблиця 4: Запитання для Астро-Вікторини
CREATE TABLE QuizQuestions (
    Id INT IDENTITY(1,1) PRIMARY KEY,
    QuestionText NVARCHAR(MAX) NOT NULL,
    AnswerA NVARCHAR(200) NOT NULL,
    AnswerB NVARCHAR(200) NOT NULL,
    AnswerC NVARCHAR(200) NOT NULL,
    AnswerD NVARCHAR(200) NOT NULL,
    CorrectAnswer NVARCHAR(1) NOT NULL
);

-- Таблиця 5: Важливі космічні місії та відкриття
CREATE TABLE CosmicMissions (
    Id INT IDENTITY(1,1) PRIMARY KEY,
    DiscoveryName NVARCHAR(150) NOT NULL,
    MissionOrShuttle NVARCHAR(150) NOT NULL,
    DiscoveryYear INT NOT NULL,
    Description NVARCHAR(MAX) NOT NULL,
    ImagePath NVARCHAR(255) NULL
);
GO

